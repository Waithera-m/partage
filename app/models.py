class Quote:

    '''
    class facilitates the creation of quote objects
    '''

    def __init__(self,id,author,quote):
        
        '''
        function facilitates the creation of quote properties

        Args:
            self.id: quote's id
            self.author:quote's author
            self.quote:quote content
        '''
        self.id = id
        self.author = author
        self.quote = quote


